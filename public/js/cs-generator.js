/**
 * Emit C# source for the root aggregator class plus one class per SchemaNode.
 * @param {SchemaNode[]} nodes
 * @param {SourceFormat} [sourceFormat] - controls which Load*() method is emitted
 * @returns {string} complete .cs file contents
 */
function generateCSharp(nodes, sourceFormat = "xlsx") {
  // Resolve valid C# identifiers for every node/property before emission.
  validateCsNames(nodes);

  const w = new CodeWriter();

  // Auto-generated header (#57)
  const version = typeof CONFORMMOLD_VERSION !== "undefined" ? CONFORMMOLD_VERSION : "dev";
  w.write("// <auto-generated>")
   .write(`//     Generated by ConFigTree ${version}`)
   .write("//     https://rpapub.github.io/ConFigTree/")
   .write("// </auto-generated>")
   .blank();

  // Nullable annotations context — required for T? Value on OrchestratorAsset<T> (#52)
  w.write("#nullable enable");
  w.blank();

  // Usings
  w.write("using System;");
  if (config.generateToJson)                                     w.write("using System.Text.Json;");
  if (config.generateLoader && sourceFormat === "xlsx")          w.write("using System.Data;");
  if (config.generatePristine)                                   w.write("using System.Collections.Generic;").write("using System.Linq;");
  if (config.generateLoader && !config.generatePristine)         w.write("using System.Collections.Generic;");
  w.blank();

  w.write(`namespace ${config.namespace}`).write("{").indent();

  // Root aggregator class
  if (config.xmlDocComments) w.write("/// <summary>Root configuration object.</summary>");
  w.write(`public class ${config.rootClassName}`).write("{").indent();

  for (const node of nodes) {
    w.write(`public ${node._className} ${node._propertyName} { get; set; } = new();`);
  }

  if (config.generateToString) {
    const props = nodes.map((n) => {
      const p = n._propertyName;
      return `${p}={${p}}`;
    }).join(", ");
    w.write(`public override string ToString() =>`).indent();
    w.write(`$"${config.rootClassName} {{ ${props} }}";`).dedent();
  }

  if (config.generateToJson) {
    w.write(`public string ToJson() => JsonSerializer.Serialize(this);`);
  }

  if (config.generateLoader) {
    w.blank();
    const rootClass = config.rootClassName;
    if (sourceFormat === "xlsx") {
      // xlsx: Load from pre-read DataTables (one per sheet)
      w.write(`public static ${rootClass} Load(Dictionary<string, DataTable> tables)`);
      w.write("{").indent();
      w.write(`var cfg = new ${rootClass}();`);
      for (const node of nodes) {
        if (node.isAssetSheet) continue;
        const propName  = node._propertyName;
        const cls       = node._className;
        const varName   = `t_${propName}`;
        const hasRows   = node.properties.length > 0 || node.children.length > 0;
        if (hasRows) {
          w.write(`if (tables.TryGetValue("${node.name}", out var ${varName})) cfg.${propName} = ${cls}.FromDataTable(${varName});`);
        }
      }
      const assetNodes = nodes.filter(n => n.isAssetSheet);
      if (assetNodes.length > 0) {
        const names = assetNodes.map(n => `"${n.name}"`).join(", ");
        w.write(`// ${names}: asset values are fetched from Orchestrator via GetRobotAsset in the generated XAML snippet — not loaded from DataTable.`);
      }
      w.write("return cfg;");
      w.dedent().write("}");
    } else if (sourceFormat === "json") {
      // JSON: deserialize directly with System.Text.Json
      w.write(`public static ${rootClass} LoadJson(string filePath)`);
      w.write("{").indent();
      w.write("var json = System.IO.File.ReadAllText(filePath);");
      w.write(`return System.Text.Json.JsonSerializer.Deserialize<${rootClass}>(json)`);
      w.write(`    ?? throw new InvalidOperationException("Failed to deserialize config.");`);
      w.dedent().write("}");
    } else if (sourceFormat === "toml") {
      // TOML: requires NuGet package Tomlyn
      w.write("// Requires NuGet: Tomlyn");
      w.write(`public static ${rootClass} LoadToml(string filePath)`);
      w.write("{").indent();
      w.write("var toml = System.IO.File.ReadAllText(filePath);");
      w.write(`return Tomlyn.TomlSerializer.Deserialize<${rootClass}>(toml);`);
      w.dedent().write("}");
    } else if (sourceFormat === "yaml") {
      // YAML: requires NuGet package YamlDotNet
      w.write("// Requires NuGet: YamlDotNet");
      w.write(`public static ${rootClass} LoadYaml(string filePath)`);
      w.write("{").indent();
      w.write("var yaml = System.IO.File.ReadAllText(filePath);");
      w.write("var deserializer = new YamlDotNet.Serialization.DeserializerBuilder()");
      w.write("    .WithNamingConvention(YamlDotNet.Serialization.NamingConventions.PascalCaseNamingConvention.Instance)");
      w.write("    .Build();");
      w.write(`return deserializer.Deserialize<${rootClass}>(yaml);`);
      w.dedent().write("}");
    }
  }

  if (config.generatePristine) {
    w.blank();
    w.write("public static readonly IReadOnlyDictionary<string, IReadOnlyList<string>> Schema =");
    w.indent().write("new Dictionary<string, IReadOnlyList<string>>").write("{").indent();
    const paths = [];
    for (const node of nodes) collectSchemaPaths(node, "", paths);
    for (const entry of paths) w.write(entry);
    w.dedent().write("};").dedent();
    w.blank();
    w.write("public DriftReport CheckPristine(IDictionary<string, IEnumerable<string>> actualKeys)");
    w.write("{").indent();
    w.write("var missing = new List<string>();");
    w.write("var extra   = new List<string>();");
    w.write("foreach (var (sheet, expected) in Schema)");
    w.write("{").indent();
    w.write("var actual      = actualKeys.TryGetValue(sheet, out var k)");
    w.write("    ? new HashSet<string>(k) : new HashSet<string>();");
    w.write("var expectedSet = new HashSet<string>(expected);");
    w.write(`missing.AddRange(expectedSet.Where(e => !actual.Contains(e)).Select(e => $"{sheet}.{e}"));`);
    w.write(`extra.AddRange(actual.Where(a => !expectedSet.Contains(a)).Select(a => $"{sheet}.{a}"));`);
    w.dedent().write("}");
    w.write("return new DriftReport(missing, extra);");
    w.dedent().write("}");
  }

  w.dedent().write("}");

  // Section classes — emitted immediately after root class so developer reads top-down (#30)
  for (const node of nodes) {
    emitClass(w, node, sourceFormat);
  }

  // DriftReport class — infrastructure; emitted last when generatePristine is on
  if (config.generatePristine) {
    w.blank();
    w.write("public class DriftReport").write("{").indent();
    w.write("public IReadOnlyList<string> MissingKeys { get; }");
    w.write("public IReadOnlyList<string> ExtraKeys   { get; }");
    w.write("public bool IsPristine => !MissingKeys.Any() && !ExtraKeys.Any();");
    w.blank();
    w.write("public DriftReport(IReadOnlyList<string> missing, IReadOnlyList<string> extra)");
    w.write("{").indent();
    w.write("MissingKeys = missing;");
    w.write("ExtraKeys   = extra;");
    w.dedent().write("}");
    w.blank();
    w.write("public override string ToString() =>").indent();
    w.write("IsPristine");
    w.write(`    ? "Pristine"`);
    w.write(`    : $"Drift detected — Missing: [{string.Join(", ", MissingKeys)}] Extra: [{string.Join(", ", ExtraKeys)}]";`);
    w.dedent();
    w.dedent().write("}");
  }

  w.dedent().write("}");
  return w.toString();
}

/**
 * Recursively emit one C# class — including FromDataTable/ToXxx/ToString as applicable.
 * Children are emitted as nested classes inside their parent (Option A).
 * Only `xlsx` emits FromDataTable(); other formats deserialize at the root level.
 * @param {CodeWriter} w
 * @param {SchemaNode} node
 * @param {SourceFormat} [sourceFormat]
 */
function emitClass(w, node, sourceFormat = "xlsx") {
  const className = node._className;
  w.blank().write(`public class ${className}`).write("{").indent();

  if (node.properties.length === 0 && node.children.length === 0) {
    w.write("// No data rows found in source sheet.");
  } else {
    // Leaf properties
    for (const prop of node.properties) {
      if (config.xmlDocComments && prop.description)
        w.write(`/// <summary>${escapeXml(prop.description)}</summary>`);
      const propName = prop._propertyName;
      const accessor = config.generateReadonly ? "init" : "set";
      if (prop.isAsset) {
        // Skip deserialization for non-xlsx sources — asset values come from
        // Orchestrator via GetRobotAsset in the XAML snippet, not from the file.
        // Tomlyn respects JsonIgnore per its System.Text.Json interop.
        if (sourceFormat === "toml" || sourceFormat === "json") {
          w.write("[System.Text.Json.Serialization.JsonIgnore]");
        } else if (sourceFormat === "yaml") {
          w.write("[YamlDotNet.Serialization.YamlIgnore]");
        }
        const vt = prop.valueType ?? "object";
        const csType = vt === "string" ? "string" : vt === "int" ? "int" : vt === "bool" ? "bool" : "object?";
        const init = csType === "string" ? '= ""' : "";
        w.write(`public ${csType} ${propName} { get; ${accessor}; }${init ? ` ${init};` : ""}`);
      } else if (prop.isCredentialRef) {
        w.write(`public string ${propName} { get; ${accessor}; } = "";`);
        w.write(`public string ${propName}Folder => ${propName}.Contains('/') ? ${propName}.Split('/')[0] : "";`);
        w.write(`public string ${propName}Name   => ${propName}.Contains('/') ? ${propName}.Split('/')[1] : ${propName};`);
      } else {
        const def = defaultInitializer(prop.csType, prop.defaultValue);
        w.write(`public ${prop.csType} ${propName} { get; ${accessor}; }${def ? ` ${def};` : ""}`);
      }
    }
    // Child section properties (reference nested classes defined below)
    for (const child of node.children) {
      const accessor = config.generateReadonly ? "init" : "set";
      w.write(`public ${child._className} ${child._propertyName} { get; ${accessor}; } = new();`);
    }
    // Nested class definitions (Option A — scoped inside parent)
    for (const child of node.children) {
      emitClass(w, child, sourceFormat);
    }

    // DataTable loader (#27) — xlsx only; JSON/TOML/YAML deserialize at root level
    // Asset sheets have no runtime data to load — values come from GetRobotAsset in XAML (#71)
    if (config.generateLoader && sourceFormat === "xlsx" && !node.isAssetSheet) {
      const cls = node._className;
      w.blank();
      w.write(`public static ${cls} FromDataTable(DataTable dt)`);
      w.write("{").indent();

      if (config.generateReadonly) {
        // Collect into locals first, then construct with object initializer (#38)
        for (const prop of node.properties) {
          const propName = prop._propertyName;
          if (prop.isAsset) {
            const vt0 = prop.valueType ?? "object";
            const ct0 = vt0 === "string" ? "string" : vt0 === "int" ? "int" : vt0 === "bool" ? "bool" : "object?";
            const z0  = ct0 === "string" ? '""' : ct0 === "int" ? "0" : ct0 === "bool" ? "false" : "null";
            w.write(`${ct0} loc_${propName} = ${z0};`);
          } else {
            const zero = { string: '""', int: "0", double: "0.0", bool: "false" }[prop.csType] ?? "default";
            w.write(`${prop.csType} loc_${propName} = ${zero};`);
          }
        }
        w.write("foreach (DataRow row in dt.Rows)");
        w.write("{").indent();
        w.write("if (row.ItemArray.Length < 2) continue;");
        w.write("var key   = row[0]?.ToString()?.Trim();");
        w.write(`var value = row[1]?.ToString()?.Trim() ?? "";`);
        w.write("switch (key)").write("{").indent();
        for (const prop of node.properties) {
          const propName = prop._propertyName;
          if (prop.isAsset) {
            // asset values are populated by GetRobotAsset in XAML, not from DataTable (#71)
          } else if (prop.csType === "string") {
            w.write(`case "${prop.name}": loc_${propName} = value; break;`);
          } else if (prop.csType === "int") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (int.TryParse(value, System.Globalization.NumberStyles.Integer, System.Globalization.CultureInfo.InvariantCulture, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "double") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (double.TryParse(value, System.Globalization.NumberStyles.Any, System.Globalization.CultureInfo.InvariantCulture, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "bool") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (bool.TryParse(value, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "DateOnly") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (DateOnly.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write(`else if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var dt_${propName})) loc_${propName} = DateOnly.FromDateTime(dt_${propName});`);
            w.write("break;").dedent();
          } else if (prop.csType === "DateTime") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "TimeOnly") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (TimeOnly.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var tmp_${propName})) loc_${propName} = tmp_${propName};`);
            w.write(`else if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var dt_${propName})) loc_${propName} = TimeOnly.FromDateTime(dt_${propName});`);
            w.write("break;").dedent();
          }
        }
        w.dedent().write("}");
        w.dedent().write("}");
        w.write(`return new ${cls}`);
        w.write("{").indent();
        for (const prop of node.properties) {
          const propName = prop._propertyName;
          w.write(`${propName} = loc_${propName},`);
        }
        w.dedent().write("};");
      } else {
        w.write(`var cfg = new ${cls}();`);
        w.write("foreach (DataRow row in dt.Rows)");
        w.write("{").indent();
        w.write("if (row.ItemArray.Length < 2) continue;");
        w.write("var key   = row[0]?.ToString()?.Trim();");
        w.write(`var value = row[1]?.ToString()?.Trim() ?? "";`);
        w.write("switch (key)").write("{").indent();
        for (const prop of node.properties) {
          const propName = prop._propertyName;
          if (prop.isAsset) {
            // asset values are populated by GetRobotAsset in XAML, not from DataTable (#71)
          } else if (prop.csType === "string") {
            w.write(`case "${prop.name}": cfg.${propName} = value; break;`);
          } else if (prop.csType === "int") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (int.TryParse(value, System.Globalization.NumberStyles.Integer, System.Globalization.CultureInfo.InvariantCulture, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "double") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (double.TryParse(value, System.Globalization.NumberStyles.Any, System.Globalization.CultureInfo.InvariantCulture, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "bool") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (bool.TryParse(value, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "DateOnly") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (DateOnly.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write(`else if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var dt_${propName})) cfg.${propName} = DateOnly.FromDateTime(dt_${propName});`);
            w.write("break;").dedent();
          } else if (prop.csType === "DateTime") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write("break;").dedent();
          } else if (prop.csType === "TimeOnly") {
            w.write(`case "${prop.name}":`).indent();
            w.write(`if (TimeOnly.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var v_${propName})) cfg.${propName} = v_${propName};`);
            w.write(`else if (DateTime.TryParse(value, System.Globalization.CultureInfo.InvariantCulture, System.Globalization.DateTimeStyles.None, out var dt_${propName})) cfg.${propName} = TimeOnly.FromDateTime(dt_${propName});`);
            w.write("break;").dedent();
          }
        }
        w.dedent().write("}");
        w.dedent().write("}");
        w.write("return cfg;");
      }

      w.dedent().write("}");
    }

    // ToXxx() mapping method (#79) — emitted when _TargetType directive is present in sheet
    if (node.targetType) {
      const outerType      = node.targetType;
      const outerClassName = outerType.split('.').pop();
      const methodName     = 'To' + outerClassName;
      const mappedProps    = node.properties.filter(p => !p.isAsset);
      w.blank();
      w.write(`public ${outerType} ${methodName}() =>`).indent();
      w.write(`new ${outerType}`);
      w.write("{").indent();
      if (node.targetProperty && node.targetInnerType) {
        // Two-level: leaf properties wrapped inside the named inner object
        w.write(`${node.targetProperty} = new ${node.targetInnerType}`);
        w.write("{").indent();
        for (const prop of mappedProps) {
          const pn = prop._propertyName;
          const dv = prop.defaultValue;
          if (prop.csType === "string" && dv != null && String(dv).trim() !== "") {
            const escaped = String(dv).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
            w.write(`${pn} = string.IsNullOrEmpty(${pn}) ? "${escaped}" : ${pn},`);
          } else {
            w.write(`${pn} = ${pn},`);
          }
        }
        w.dedent().write("}");
      } else {
        // Flat — existing behaviour
        for (const prop of mappedProps) {
          const pn = prop._propertyName;
          const dv = prop.defaultValue;
          if (prop.csType === "string" && dv != null && String(dv).trim() !== "") {
            const escaped = String(dv).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
            w.write(`${pn} = string.IsNullOrEmpty(${pn}) ? "${escaped}" : ${pn},`);
          } else {
            w.write(`${pn} = ${pn},`);
          }
        }
      }
      w.dedent().write("};").dedent();
    }

    // ToString() for sub-class — lists leaf properties so ConFigTree.ToString() is useful (#46)
    if (config.generateToString && node.properties.length > 0) {
      const className = node._className;
      const parts = node.properties.map(p => {
        const pn = p._propertyName;
        return `${pn}={${pn}}`;
      }).join(", ");
      w.blank();
      w.write("public override string ToString() =>").indent();
      w.write(`$"${className} {{ ${parts} }}";`).dedent();
    }
  }

  w.dedent().write("}");
}

/**
 * Collect `"Section.Path" → key list` entries for the Schema manifest (#22).
 * Flat xlsx nodes use `node.name`; nested nodes use a dotted `Parent.Child` path.
 * @param {SchemaNode} node
 * @param {string} prefix - dotted path accumulated during recursion
 * @param {string[]} out - output array, appended in place
 */
function collectSchemaPaths(node, prefix, out) {
  const path = prefix ? `${prefix}.${node.name}` : node.name;
  if (node.children.length > 0) {
    for (const child of node.children) collectSchemaPaths(child, path, out);
  } else {
    const keys = node.properties.map((p) => `"${p.name}"`).join(", ");
    out.push(`["${path}"] = new[] { ${keys} },`);
  }
}

/**
 * Convert a sheet/section name into a PascalCase `…Config` class name.
 * @param {string} name
 * @returns {string}
 */
function toClassName(name) {
  return name.split(/[.\-_]/).map(toPascalCase).join("") + "Config";
}

/**
 * Normalize an identifier by splitting on `_ - . whitespace` and PascalCasing parts.
 * @param {string} str
 * @returns {string}
 */
function toPascalCase(str) {
  return str
    .replace(/[_\-]/g, " ")
    .split(/[\s.]+/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");
}

/**
 * Coerce `raw` into a valid C# identifier. Deterministic given `raw`; if a
 * `used` Set is provided, disambiguates collisions by appending `_2`, `_3`, …
 * and adds the final value to the Set.
 *
 * Rules:
 * - Runs of chars outside `[A-Za-z0-9_]` collapse to a single `_`
 * - Leading / trailing `_` trimmed
 * - Leading digit prefixed with `_`
 * - Empty result falls back to `"Identifier"`
 *
 * @param {string} raw
 * @param {Set<string>} [used] - scope for collision disambiguation
 * @returns {string}
 */
function sanitizeCsIdentifier(raw, used) {
  let id = String(raw).replace(/[^A-Za-z0-9_]+/g, "_").replace(/^_+|_+$/g, "");
  if (/^[0-9]/.test(id)) id = "_" + id;
  if (!id) id = "Identifier";
  if (used) {
    const base = id;
    let n = 2;
    while (used.has(id)) id = `${base}_${n++}`;
    used.add(id);
  }
  return id;
}

/**
 * Pre-emission pass: walk the node tree, compute valid C# identifiers for
 * every class and property, stash them on `node._className` / `node._propertyName`
 * / `prop._propertyName` / `child._className` / `child._propertyName`, and push
 * a warning on `node.warnings` whenever sanitization or disambiguation changed
 * the result.
 *
 * Scopes:
 * - Top-level class names compete in the namespace scope
 * - Root-aggregator property names compete in the root-class property scope
 * - A node's own properties and child-refs compete in its class's property scope
 * - Nested class names compete in their parent class's nested-class scope
 *
 * @param {SchemaNode[]} nodes
 */
function validateCsNames(nodes) {
  const classScope    = new Set();
  const rootPropScope = new Set();
  for (const node of nodes) {
    node.warnings = node.warnings || [];

    // Root-aggregator property name: sanitize the PascalCased base first
    const naiveProp    = toPascalCase(node.name);
    node._propertyName = sanitizeCsIdentifier(naiveProp, rootPropScope);
    if (node._propertyName !== naiveProp) {
      node.warnings.push(`Section "${node.name}": root-aggregator property sanitized to '${node._propertyName}' (was '${naiveProp || "<empty>"}').`);
    }

    // Class name = sanitized-base + "Config". Disambiguated against all other classes.
    const candidateClass = node._propertyName + "Config";
    node._className      = sanitizeCsIdentifier(candidateClass, classScope);
    if (node._className !== candidateClass) {
      node.warnings.push(`Section "${node.name}": class name disambiguated to '${node._className}' (was '${candidateClass}').`);
    }

    validateNodeMembers(node);
  }
}

/**
 * Populate `_propertyName` on each property and `_className` / `_propertyName`
 * on each child of `node`. Recurse into children.
 * @param {SchemaNode} node
 */
function validateNodeMembers(node) {
  const propScope        = new Set();
  const nestedClassScope = new Set();

  for (const prop of node.properties || []) {
    const naive = toPascalCase(prop.name);
    prop._propertyName = sanitizeCsIdentifier(naive, propScope);
    if (prop._propertyName !== naive) {
      node.warnings.push(`Class ${node._className}: property '${prop.name}' sanitized to '${prop._propertyName}' (was '${naive || "<empty>"}').`);
    }
  }

  for (const child of node.children || []) {
    child.warnings = child.warnings || [];

    const naiveChildProp = toPascalCase(child.name);
    child._propertyName  = sanitizeCsIdentifier(naiveChildProp, propScope);
    if (child._propertyName !== naiveChildProp) {
      node.warnings.push(`Class ${node._className}: child property '${child.name}' sanitized to '${child._propertyName}' (was '${naiveChildProp || "<empty>"}').`);
    }

    const candidateChildClass = child._propertyName + "Config";
    child._className          = sanitizeCsIdentifier(candidateChildClass, nestedClassScope);
    if (child._className !== candidateChildClass) {
      node.warnings.push(`Class ${node._className}: nested class '${child.name}' disambiguated to '${child._className}' (was '${candidateChildClass}').`);
    }

    validateNodeMembers(child);
  }
}

/**
 * Emit the C# ` = value` suffix for a property given its CsType and default.
 * Returns an empty string when no initializer should be emitted.
 * @param {CsType} csType
 * @param {*} dv - default value from the parser (string, number, boolean, Date, or null)
 * @returns {string}
 */
function defaultInitializer(csType, dv) {
  if (csType === "string") {
    if (dv != null && String(dv) !== "") {
      const esc = String(dv).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
      return `= "${esc}"`;
    }
    return '= ""';
  }
  if (dv == null) return "";
  if (csType === "bool")   return `= ${dv ? "true" : "false"}`;
  if (csType === "int")    return `= ${Math.trunc(Number(dv))}`;
  if (csType === "double") {
    const n = Number(dv);
    return `= ${Number.isInteger(n) ? n + ".0" : n}`;
  }
  if (dv instanceof Date) {
    const yr = dv.getUTCFullYear(), mo = dv.getUTCMonth() + 1, dy = dv.getUTCDate();
    const hr = dv.getUTCHours(), mn = dv.getUTCMinutes(), sc = dv.getUTCSeconds();
    if (csType === "TimeOnly") return `= new TimeOnly(${hr}, ${mn}, ${sc})`;
    if (csType === "DateTime") return `= new DateTime(${yr}, ${mo}, ${dy}, ${hr}, ${mn}, ${sc})`;
    if (csType === "DateOnly") return `= new DateOnly(${yr}, ${mo}, ${dy})`;
  }
  return "";
}

